package com.diffusioncraft.plugin;

public class ColoredPoint {
    double x, y, z, r, g, b;

    public ColoredPoint(double x, double y, double z, double r, double g, double b) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.r = r;
        this.g = g;
        this.b = b;
    }
}
